// Creation of object and access

//  Using "{}" curly brackets 

// Create an object of book with properties like title, author, and year
const book = {
    title: "titleOfBook",
    author: "authorOfBook",
    year: "publishYearOfBook"
}

console.log(book);

// Access the title and author properties of the book object
console.log(book.title);
console.log(book.author);
