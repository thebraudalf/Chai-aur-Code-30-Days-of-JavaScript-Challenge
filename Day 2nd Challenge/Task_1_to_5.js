// Performing Arithmetic operations
let num1 = 10;
let num2 = 5;

// Using + operator
let add = num1 + num2;
console.log(add);

// Using - operator
let sub = num1 - num2;
console.log(sub);

// Using * operator
let mul = num1 * num2;
console.log(mul);

// Using / operator
let div = num1 / num2;
console.log(div);

// Using % operator to find reminder
let rem = num1 % num2;
console.log(rem);
