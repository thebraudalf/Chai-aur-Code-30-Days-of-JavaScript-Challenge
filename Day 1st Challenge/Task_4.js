// Creation of variables of different data types
// Number data type
let testNum = 24;
console.log(testNum); //24
console.log(typeof testNum); //number

// String data type 
let testStr = "This is test";
console.log(testStr); //This is test
console.log(typeof testStr); //string

// Boolean data type 
const testBool = true;
console.log(testBool); //true
console.log(typeof testBool); //boolean

// Object data type 
let testObj = {obj: "This is the test of object"};
console.log(testObj); //{ obj: 'This is the test of object' }
console.log(typeof testObj); //object 

// Array data type 
let testArr = [1, 2, 3, 4, 5];
console.log(testArr); //[ 1, 2, 3, 4, 5 ]
console.log(typeof testArr); //object

